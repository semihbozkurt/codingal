print("---and operator table---")
print(f"1 & 1 : {1 & 1}")
print(f"1 & 0 : {1 & 0}")
print(f"0 & 1 : {0 & 1}")
print(f"0 & 0 : {0 & 0}")

print("---or operator table---")
print(f"1 | 1 : {1 | 1}")
print(f"1 | 0 : {1 | 0}")
print(f"0 | 1 : {0 | 1}")
print(f"0 | 0 : {0 | 0}")


print("---xor operator table---")
print(f"1 ^ 1 : {1 ^ 1}")
print(f"1 ^ 0 : {1 ^ 0}")
print(f"0 ^ 1 : {0 ^ 1}")
print(f"0 ^ 0 : {0 ^ 0}")
print("---NOT operator table---")
print(f"NOT 1 : {~1}") # 0
print(f"NOT 0 : {~0}") # 1
# << (left shift operator )   , >> (rigth shift operator)
print("-----<< left shift operator----")
print(16<<3)
print("----->> right shift operator----")
print(16>>2)
